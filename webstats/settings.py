# Cache
# Por defecto Django usa DummyCache si no se define CACHES (no cachea nada) y las vistas de métricas
# recalculan todo en cada request.
#
# Importante: FileBasedCache puede bloquearse o ir lento en algunos entornos (I/O / locks).
# Usamos LocMemCache por defecto (rápido) y dejamos FileBasedCache como opción por env.
# Nota: LocMemCache NO se comparte entre procesos (gunicorn workers). En producción esto provoca
# que invalidaciones (p.ej. edición manual de estadísticas) no se reflejen si el siguiente request
# cae en otro worker con caché "vieja".
#
# Rendimiento: en Render solemos ejecutar con `WEB_CONCURRENCY=1`, así que LocMemCache es el backend
# más rápido y suficiente. Si hay >1 worker y no se especifica `CACHE_BACKEND`, usamos FileBasedCache
# como opción "shared" sin dependencias externas (aunque sea más lenta).
try:
    _web_concurrency = int(os.getenv("WEB_CONCURRENCY") or "1")
except Exception:
    _web_concurrency = 1
_default_cache_backend = 'locmem' if DEBUG else ('locmem' if _web_concurrency <= 1 else 'filebased')
CACHE_BACKEND = (os.getenv('CACHE_BACKEND') or _default_cache_backend).strip().lower()
CACHE_DEFAULT_TIMEOUT = _env_int('CACHE_DEFAULT_TIMEOUT', 3600)
_cache_max_entries = _env_int('CACHE_MAX_ENTRIES', 10000)
if CACHE_BACKEND in {'file', 'filebased'}:
    CACHE_DIR = (os.getenv('CACHE_DIR') or '/tmp/webstats-cache').strip() or '/tmp/webstats-cache'
    try:
        os.makedirs(CACHE_DIR, exist_ok=True)
    except Exception:
        pass
    CACHES = {
        'default': {
            'BACKEND': 'django.core.cache.backends.filebased.FileBasedCache',
            'LOCATION': CACHE_DIR,
            'TIMEOUT': CACHE_DEFAULT_TIMEOUT,
            'OPTIONS': {
                'MAX_ENTRIES': _cache_max_entries,
            },
        }
    }
else:
    CACHES = {
        'default': {
            'BACKEND': 'django.core.cache.backends.locmem.LocMemCache',
            'LOCATION': 'webstats-locmem',
            'TIMEOUT': CACHE_DEFAULT_TIMEOUT,
            'OPTIONS': {
                'MAX_ENTRIES': _cache_max_entries,
            },
        }
    }


LOGGING = {
    'version': 1,
    'disable_existing_loggers': False,
    'formatters': {
        'verbose': {
            'format': '{levelname} {asctime} {module} {message}',
            'style': '{',
        },
    },
    'handlers': {
        'file': {
            'level': 'ERROR',
            'class': 'logging.FileHandler',
            'filename': BASE_DIR / 'django_error.log',
            'formatter': 'verbose',
        },
        'console': {
            'level': 'ERROR',
            'class': 'logging.StreamHandler',
            'formatter': 'verbose',
        },
        'console_perf': {
            'level': 'WARNING',
            'class': 'logging.StreamHandler',
            'formatter': 'verbose',
        },
    },
    'loggers': {
        'django': {'handlers': ['file'], 'level': 'ERROR', 'propagate': True},
        'django.request': {'handlers': ['file'], 'level': 'ERROR', 'propagate': True},
        # Performance diagnostics (SlowRequestLoggingMiddleware / ServerTimingMiddleware)
        'webstats.perf': {'handlers': ['console_perf'], 'level': 'WARNING', 'propagate': False},
    },
}

# In Render (and most container platforms), stdout/stderr is where logs are collected.
# Also keep file logging to allow quick `tail django_error.log` in shells.
_log_to_stdout = os.getenv('LOG_TO_STDOUT', '').strip().lower() in {'1', 'true', 'yes', 'on'}
_is_render = bool((os.getenv('RENDER') or os.getenv('RENDER_GIT_COMMIT') or os.getenv('RENDER_SERVICE_ID') or '').strip())
if _log_to_stdout or _is_render:
    try:
        for key in ('django', 'django.request'):
            handlers = LOGGING.get('loggers', {}).get(key, {}).get('handlers', [])
            if 'console' not in handlers:
                handlers.append('console')
    except Exception:
        pass
